const quicksort = require("./lib");

test("quicksort", () => {
  const input = [
    44, 22, 15, 48, 17, 85, 69, 26, 45, 88, 30, 92, 57, 19, 52, 70, 72, 73, 100,
    70, 59, 52, 27, 49, 64, 9, 86, 85, 100, 8, 88, 11, 25, 19, 12, 73, 71, 31,
    98, 37, 60, 55, 3, 87, 4, 86, 35, 70, 48, 97,
  ];
  const result = [
    3, 4, 8, 9, 11, 12, 15, 17, 19, 19, 22, 25, 26, 27, 30, 31, 35, 37, 44, 45,
    48, 48, 49, 52, 52, 55, 57, 59, 60, 64, 69, 70, 70, 70, 71, 72, 73, 73, 85,
    85, 86, 86, 87, 88, 88, 92, 97, 98, 100, 100,
  ];
  expect(quicksort(input)).toStrictEqual(result);
});
